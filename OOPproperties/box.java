package OOPproperties;

public class box {
    int len;
    int width;
    int height;

    box(){
        this.len=-1;
        this.width=-1;
        this.height=-1;
    }

    public box(int len, int width, int height) {
        this.len = len;
        this.width = width;
        this.height = height;
    }
}
